<h3>THE STORY SO FAR</h3>  
<p>It has been suggested variously that extinction events occurred periodically, every 26 to 30 million years,[38] or that diversity fluctuates episodically every ~62 million years.[39] Various ideas attempt to explain the supposed pattern, including the presence of a hypothetical companion star to the sun,[40] [41] oscillations in the galactic plane, or passage through the Milky Way's spiral arms.[42] However, other authors have concluded the data on marine mass extinctions do not fit with the idea that mass extinctions are periodic, or that ecosystems gradually build up to a point at which a mass extinction is inevitable.[18] Many of the proposed correlations have been argued to be spurious.[43][44] Others have argued that there is strong evidence supporting periodicity in a variety of records, [45] and additional evidence in the form of coincident periodic variation in nonbiological geochemical variables. [46] Mass extinctions are thought to result when a long-term stress is compounded by a short term shock.[47] Over the course of the Phanerozoic, individual taxa appear to be less likely to become extinct at any time,[48] which may reflect more robust food webs as well as less extinction-prone species and other factors such as continental distribution.[48] However, even after accounting for sampling bias, there does appear to be a gradual decrease in extinction and origination rates during the Phanerozoic.[18] This may represent the fact that groups with higher turnover rates are more likely to become extinct by chance; or it may be an artefact of taxonomy: families tend to become more speciose, therefore less prone to extinction, over time;[18] and larger taxonomic groups (by definition) appear earlier in geological time.[49]</p>

<p>It has also been suggested that the oceans have gradually become more hospitable to life over the last 500 million years, and thus less vulnerable to mass extinctions,[note 1][50][51] but susceptibility to extinction at a taxonomic level does not appear to make mass extinctions more or less probable.[48]
Causes</p>

<p>There is still debate about the causes of all mass extinctions. In general, large extinctions may result when a biosphere under long-term stress undergoes a short-term shock.[47] An underlying mechanism appears to be present in the correlation of extinction and origination rates to diversity. High diversity leads to a persistent increase in extinction rate; low diversity to a persistent increase in origination rate. These presumably ecologically controlled relationships likely amplify smaller perturbations (asteroid impacts, etc.) to produce the global effects observed.[18]</p>

<p>Identifying causes of particular mass extinctions

A good theory for a particular mass extinction should: (i) explain all of the losses, not just focus on a few groups (such as dinosaurs); (ii) explain why particular groups of organisms died out and why others survived; (iii) provide mechanisms which are strong enough to cause a mass extinction but not a total extinction; (iv) be based on events or processes that can be shown to have happened, not just inferred from the extinction.</p>

<p>It may be necessary to consider combinations of causes. For example, the marine aspect of the end-Cretaceous extinction appears to have been caused by several processes which partially overlapped in time and may have had different levels of significance in different parts of the world.[52]</p>

<p>Arens and West (2006) proposed a "press / pulse" model in which mass extinctions generally require two types of cause: long-term pressure on the eco-system ("press") and a sudden catastrophe ("pulse") towards the end of the period of pressure.[53] Their statistical analysis of marine extinction rates throughout the Phanerozoic suggested that neither long-term pressure alone nor a catastrophe alone was sufficient to cause a significant increase in the extinction rate.
Most widely supported explanations</p>

<p>Macleod (2001)[54] summarized the relationship between mass extinctions and events which are most often cited as causes of mass extinctions, using data from Courtillot et al. (1996),[55] Hallam (1992)[56] and Grieve et al. (1996):[57]</p>

    Flood basalt events: 11 occurrences, all associated with significant extinctions[58][59] But Wignall (2001) concluded that only five of the major extinctions coincided with flood basalt eruptions and that the main phase of extinctions started before the eruptions.[60]
    Sea-level falls: 12, of which seven were associated with significant extinctions.[59]
    Asteroid impacts; One large impact associated with a mass extinction; there have been many smaller impacts but they are not associated with significant extinctions.[clarification needed]

<p>The most commonly suggested causes of mass extinctions are listed below.
Flood basalt events</p>

<p>The formation of large igneous provinces by flood basalt events could have:</p>

    produced dust and particulate aerosols which inhibited photosynthesis and thus caused food chains to collapse both on land and at sea
    emitted sulfur oxides which were precipitated as acid rain and poisoned many organisms, contributing further to the collapse of food chains
    emitted carbon dioxide and thus possibly causing sustained global warming once the dust and particulate aerosols dissipated.

<p>Flood basalt events occur as pulses of activity punctuated by dormant periods. As a result, they are likely to cause the climate to oscillate between cooling and warming, but with an overall trend towards warming as the carbon dioxide they emit can stay in the atmosphere for hundreds of years.</p>

<p>It is speculated that massive volcanism caused or contributed to the End-Permian, End-Triassic and End-Cretaceous extinctions.[61] The correlation between gigantic volcanic events expressed in the large igneous provinces and mass extinctions was shown for the last 260 Myr.[62][63] Recently such possible correlation was extended for the whole Phanerozoic Eon.[64]
Sea-level falls</p>

<p>These are often clearly marked by worldwide sequences of contemporaneous sediments which show all or part of a transition from sea-bed to tidal zone to beach to dry land – and where there is no evidence that the rocks in the relevant areas were raised by geological processes such as orogeny. Sea-level falls could reduce the continental shelf area (the most productive part of the oceans) sufficiently to cause a marine mass extinction, and could disrupt weather patterns enough to cause extinctions on land. But sea-level falls are very probably the result of other events, such as sustained global cooling or the sinking of the mid-ocean ridges.</p>

<p>Sea-level falls are associated with most of the mass extinctions, including all of the "Big Five"—End-Ordovician, Late Devonian, End-Permian, End-Triassic, and End-Cretaceous.</p>

<p>A study, published in the journal Nature (online June 15, 2008) established a relationship between the speed of mass extinction events and changes in sea level and sediment.[65] The study suggests changes in ocean environments related to sea level exert a driving influence on rates of extinction, and generally determine the composition of life in the oceans.[66]</p>
Impact events

<p>The impact of a sufficiently large asteroid or comet could have caused food chains to collapse both on land and at sea by producing dust and particulate aerosols and thus inhibiting photosynthesis. Impacts on sulfur-rich rocks could have emitted sulfur oxides precipitating as poisonous acid rain, contributing further to the collapse of food chains. Such impacts could also have caused megatsunamis and/or global forest fires.</p>

<p>Most paleontologists now agree that an asteroid did hit the Earth about 66 Ma ago, but there is an ongoing dispute whether the impact was the sole cause of the Cretaceous–Paleogene extinction event.[67][68]
Sustained and significant global cooling</p>

<p>Sustained global cooling could kill many polar and temperate species and force others to migrate towards the equator; reduce the area available for tropical species; often make the Earth's climate more arid on average, mainly by locking up more of the planet's water in ice and snow. The glaciation cycles of the current ice age are believed to have had only a very mild impact on biodiversity, so the mere existence of a significant cooling is not sufficient on its own to explain a mass extinction.</p>

<p>It has been suggested that global cooling caused or contributed to the End-Ordovician, Permian-Triassic, Late Devonian extinctions, and possibly others. Sustained global cooling is distinguished from the temporary climatic effects of flood basalt events or impacts.
Sustained and significant global warming</p>

<p>This would have the opposite effects: expand the area available for tropical species; kill temperate species or force them to migrate towards the poles; possibly cause severe extinctions of polar species; often make the Earth's climate wetter on average, mainly by melting ice and snow and thus increasing the volume of the water cycle. It might also cause anoxic events in the oceans (see below).</p>

<p>Global warming as a cause of mass extinction is supported by several recent studies.[69]</p>

<p>The most dramatic example of sustained warming is the Paleocene-Eocene Thermal Maximum, which was associated with one of the smaller mass extinctions. It has also been suggested to have caused the Triassic-Jurassic extinction event, during which 20% of all marine families went extinct. Furthermore, the Permian–Triassic extinction event has been suggested to have been caused by warming.[70][71][72] Human-caused global warming is contributing to extinctions today.
Clathrate gun hypothesis
Main article: Clathrate gun hypothesis</p>

<p>Clathrates are composites in which a lattice of one substance forms a cage around another. Methane clathrates (in which water molecules are the cage) form on continental shelves. These clathrates are likely to break up rapidly and release the methane if the temperature rises quickly or the pressure on them drops quickly—for example in response to sudden global warming or a sudden drop in sea level or even earthquakes. Methane is a much more powerful greenhouse gas than carbon dioxide, so a methane eruption ("clathrate gun") could cause rapid global warming or make it much more severe if the eruption was itself caused by global warming.</p>

<p>The most likely signature of such a methane eruption would be a sudden decrease in the ratio of carbon-13 to carbon-12 in sediments, since methane clathrates are low in carbon-13; but the change would have to be very large, as other events can also reduce the percentage of carbon-13.[73]</p>

<p>It has been suggested that "clathrate gun" methane eruptions were involved in the end-Permian extinction ("the Great Dying") and in the Paleocene–Eocene Thermal Maximum, which was associated with one of the smaller mass extinctions.</p>
Anoxic events

<p>Anoxic events are situations in which the middle and even the upper layers of the ocean become deficient or totally lacking in oxygen. Their causes are complex and controversial, but all known instances are associated with severe and sustained global warming, mostly caused by sustained massive volcanism.</p>

<p>It has been suggested that anoxic events caused or contributed to the Ordovician–Silurian, late Devonian, Permian–Triassic and Triassic–Jurassic extinctions, as well as a number of lesser extinctions (such as the Ireviken, Mulde, Lau, Toarcian and Cenomanian–Turonian events). On the other hand, there are widespread black shale beds from the mid-Cretaceous which indicate anoxic events but are not associated with mass extinctions.
Hydrogen sulfide emissions from the seas</p>

<p>Kump, Pavlov and Arthur (2005) have proposed that during the Permian–Triassic extinction event the warming also upset the oceanic balance between photosynthesising plankton and deep-water sulfate-reducing bacteria, causing massive emissions of hydrogen sulfide which poisoned life on both land and sea and severely weakened the ozone layer, exposing much of the life that still remained to fatal levels of UV radiation.[74][75][76]</p>
Oceanic overturn

<p>Oceanic overturn is a disruption of thermo-haline circulation which lets surface water (which is more saline than deep water because of evaporation) sink straight down, bringing anoxic deep water to the surface and therefore killing most of the oxygen-breathing organisms which inhabit the surface and middle depths. It may occur either at the beginning or the end of a glaciation, although an overturn at the start of a glaciation is more dangerous because the preceding warm period will have created a larger volume of anoxic water.[77]</p>

<p>Unlike other oceanic catastrophes such as regressions (sea-level falls) and anoxic events, overturns do not leave easily identified "signatures" in rocks and are theoretical consequences of researchers' conclusions about other climatic and marine events.</p>

<p>It has been suggested that oceanic overturn caused or contributed to the late Devonian and Permian–Triassic extinctions.
A nearby nova, supernova or gamma ray burst</p>

<p>A nearby gamma-ray burst (less than 6000 light years away) would be powerful enough to destroy the Earth's ozone layer, leaving organisms vulnerable to ultraviolet radiation from the sun.[78] Gamma ray bursts are fairly rare, occurring only a few times in a given galaxy per million years.[79] It has been suggested that a supernova or gamma ray burst caused the End-Ordovician extinction. [45]
Geomagnetic reversal</p>

<p>One theory is that periods of increased geomagnetic reversals will weaken Earth's magnetic field long enough to expose the atmosphere to the solar winds, causing oxygen ions to escape the atmosphere in a rate increased by 3-4 orders, resulting in a disastrous drop of oxygen.[80]
Plate tectonics</p>

<p>Movement of the continents into some configurations can cause or contribute to extinctions in several ways: by initiating or ending ice ages; by changing ocean and wind currents and thus altering climate; by opening seaways or land bridges which expose previously isolated species to competition for which they are poorly adapted (for example, the extinction of most of South America's native ungulates and all of its large metatherians after the creation of a land bridge between North and South America). Occasionally continental drift creates a super-continent which includes the vast majority of Earth's land area, which in addition to the effects listed above is likely to reduce the total area of continental shelf (the most species-rich part of the ocean) and produce a vast, arid continental interior which may have extreme seasonal variations.<p>

<p>Another theory is that the creation of the super-continent Pangaea contributed to the End-Permian mass extinction. Pangaea was almost fully formed at the transition from mid-Permian to late-Permian, and the "Marine genus diversity" diagram at the top of this article shows a level of extinction starting at that time which might have qualified for inclusion in the "Big Five" if it were not overshadowed by the "Great Dying" at the end of the Permian.[81]
Other hypotheses</p>

<p>Many other hypotheses have been proposed, such as the spread of a new disease, or simple out-competition following an especially successful biological innovation. But all have been rejected, usually for one of the following reasons: they require events or processes for which there is no evidence; they assume mechanisms which are contrary to the available evidence; they are based on other theories which have been rejected or superseded.</p>

<p>Supervolcanic events may also been potential causes of mass extinctions. While none of the extinction events in Earth's past have been caused by any supervolcanic eruptions, the Toba supereruption may have reduced the first humans down to a few thousand individuals.</p>

<p>cientists have been concerned that human activities could cause more plants and animals to become extinct than any point in the past. Along with man-made changes in climate (see above), some of these extinctions could be caused by overhunting, overfishing, invasive species, or habitat loss.
Future biosphere extinction</p>

    See also: Future of the Earth and Fermi paradox

<p>The eventual warming and expanding of the Sun, combined with the eventual decline of atmospheric carbon dioxide could actually cause an even greater mass extinction, having the potential to wipe out even microbes, where rising global temperatures caused by the expanding Sun will gradually increase the rate of weathering, which in turn removes more and more carbon dioxide from the atmosphere. When carbon dioxide levels get too low (perhaps at 50 ppm), all plant life will die out, although simpler plants like grasses and mosses can survive much longer, until CO2 levels drop to 10 ppm.[82][83]</p>

<p>With all photosynthetic organisms gone, atmospheric oxygen can no longer be replenished, and is eventually removed by chemical reactions in the atmosphere, perhaps from volcanic eruptions. Eventually the loss of oxygen will cause all remaining aerobic life to die out via asphyxiation, leaving behind only simple anaerobic prokaryotes. When the Sun becomes 10% brighter [82] Earth will suffer a moist greenhouse effect resulting in its oceans boiling away, while the Earth's liquid outer core freezes due to the inner core's expansion and causes the Earth's magnetic field to shut down. In the absence of a magnetic field, charged particles from the Sun will deplete the atmosphere and further increase the Earth's temperature to an average of ~420 K (147 °C, 296 °F), causing the last remaining life on Earth to die out. This is the most extreme instance of a climate-caused extinction event. Since this will only happen late in the Sun's life, such will cause the final mass extinction in Earth's history.[82][83]</p>
Effects and recovery

<p>The impact of mass extinction events varied widely. After a major extinction event, usually only weedy species survive due to their ability to live in diverse habitats.[84] Later, species diversify and occupy empty niches. Generally, biodiversity recovers 5 to 10 million years after the extinction event. In the most severe mass extinctions it may take 15 to 30 million years.[84]</p>

<p>The worst event, the Permian–Triassic extinction event, devastated life on earth and is estimated to have killed off over 90% of species. Life seemed to recover quickly after the P-T extinction, but this was mostly in the form of disaster taxa, such as the hardy Lystrosaurus. The most recent research indicates that the specialized animals that formed complex ecosystems, with high biodiversity, complex food webs and a variety of niches, took much longer to recover. It is thought that this long recovery was due to the successive waves of extinction which inhibited recovery, as well as to prolonged environmental stress to organisms which continued into the Early Triassic. Recent research indicates that recovery did not begin until the start of the mid-Triassic, 4M to 6M years after the extinction;[85] and some writers estimate that the recovery was not complete until 30M years after the P-Tr extinction, i.e. in the late Triassic.[86] Subsequent to the PT mass extinction, there was an increase in provincialization, with species occupying smaller ranges - perhaps removing incumbents from niches and setting the stage for an eventual rediversification.[87]</p>


<p>It has been suggested variously that extinction events occurred periodically, every 26 to 30 million years,[38] or that diversity fluctuates episodically every ~62 million years.[39] Various ideas attempt to explain the supposed pattern, including the presence of a hypothetical companion star to the sun,[40] [41] oscillations in the galactic plane, or passage through the Milky Way's spiral arms.[42] However, other authors have concluded the data on marine mass extinctions do not fit with the idea that mass extinctions are periodic, or that ecosystems gradually build up to a point at which a mass extinction is inevitable.[18] Many of the proposed correlations have been argued to be spurious.[43][44] Others have argued that there is strong evidence supporting periodicity in a variety of records, [45] and additional evidence in the form of coincident periodic variation in nonbiological geochemical variables. [46] Mass extinctions are thought to result when a long-term stress is compounded by a short term shock.[47] Over the course of the Phanerozoic, individual taxa appear to be less likely to become extinct at any time,[48] which may reflect more robust food webs as well as less extinction-prone species and other factors such as continental distribution.[48] However, even after accounting for sampling bias, there does appear to be a gradual decrease in extinction and origination rates during the Phanerozoic.[18] This may represent the fact that groups with higher turnover rates are more likely to become extinct by chance.</p>

